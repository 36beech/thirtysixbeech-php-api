CREATE VIEW top_species_by_sightings AS
SELECT
    s.common_name,
    s.scientific_name,
    f.common_name AS family,
    COUNT(sg.id)    AS sighting_events,
    SUM(sg.count)   AS total_birds_counted
FROM species s
JOIN families f  ON s.family_id = f.id
JOIN sightings sg ON sg.species_id = s.id
GROUP BY s.id, s.common_name, s.scientific_name, f.common_name
ORDER BY total_birds_counted DESC;

CREATE VIEW migratory_species_list AS
SELECT
    s.common_name,
    s.scientific_name,
    f.common_name AS family,
    s.conservation_status,
    s.avg_wingspan_cm,
    s.avg_weight_g
FROM species s
JOIN families f ON s.family_id = f.id
WHERE s.migratory = TRUE
ORDER BY f.common_name, s.common_name;

CREATE VIEW observer_leaderboard AS
SELECT
    o.name,
    o.skill_level,
    o.city,
    o.state_province,
    COUNT(sg.id)        AS sighting_events,
    SUM(sg.count)       AS total_birds,
    COUNT(DISTINCT sg.species_id) AS distinct_species
FROM observers o
LEFT JOIN sightings sg ON sg.observer_id = o.id
GROUP BY o.id, o.name, o.skill_level, o.city, o.state_province
ORDER BY total_birds DESC;

CREATE VIEW sightings_by_location AS
SELECT
    l.name AS location,
    l.state_province,
    l.habitat_type,
    COUNT(sg.id)        AS sighting_events,
    SUM(sg.count)       AS total_birds,
    COUNT(DISTINCT sg.species_id) AS distinct_species
FROM locations l
LEFT JOIN sightings sg ON sg.location_id = l.id
GROUP BY l.id, l.name, l.state_province, l.habitat_type
ORDER BY total_birds DESC;
