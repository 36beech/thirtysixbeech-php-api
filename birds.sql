-- -----------------------------------------------------------
-- SPECIES
-- -----------------------------------------------------------
CREATE TABLE species (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    family_id       INT NOT NULL,
    common_name     VARCHAR(120) NOT NULL,
    scientific_name VARCHAR(120) NOT NULL,
    conservation_status ENUM('LC','NT','VU','EN','CR','EW','EX') DEFAULT 'LC',
    avg_wingspan_cm DECIMAL(6,1),
    avg_weight_g    DECIMAL(8,1),
    migratory       BOOLEAN DEFAULT FALSE,
    habitat         VARCHAR(200),
    FOREIGN KEY (family_id) REFERENCES families(id)
);

